package com.example.recyclerviewwithmultipleviewtypes;

public class ChildModal {

    int image;
    String text;

    public ChildModal(int image, String text) {
        this.image = image;
        this.text = text;
    }

    public int getImage() {
        return image;
    }

    public String getText() {
        return text;
    }
}
